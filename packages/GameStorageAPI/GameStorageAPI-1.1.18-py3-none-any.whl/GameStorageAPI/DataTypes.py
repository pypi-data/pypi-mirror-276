def BringYandX(ListOfLints: list[str]) -> list[str]:
    fulldec = []
    for i in range(len(ListOfLints)):
        minir = []
        for j in range(3):
            if ListOfLints[i][j] != None:
                if j == 1 and i not in [14, 15]:
                    minir.append(os.getenv(GetAny(LocalBall)) + GetAny(ListOfLints[i][j]))
                elif j == 1 and i in [14, 15]:
                    minir.append(os.getenv(GetAny(DataAboutBall)) + GetAny(ListOfLints[i][j]))
                else:
                    minir.append(GetAny(ListOfLints[i][j]))
            else:
                minir.append(None)
        fulldec.append(minir)
    return fulldec

